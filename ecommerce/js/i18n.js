// ── i18n Engine ─────────────────────────────────────────────────────────────
const FaizI18n = {
  currentLang: localStorage.getItem("faiz_lang") || FAIZ_CONFIG.DEFAULT_LANG,

  t(key, vars = {}) {
    const dict = FAIZ_I18N[this.currentLang] || FAIZ_I18N["id"];
    let str = dict[key] || FAIZ_I18N["en"][key] || key;
    Object.entries(vars).forEach(([k, v]) => {
      str = str.replace(`{${k}}`, v);
    });
    return str;
  },

  setLang(lang) {
    this.currentLang = lang;
    localStorage.setItem("faiz_lang", lang);
    this.applyToPage();
  },

  applyToPage() {
    document.querySelectorAll("[data-i18n]").forEach((el) => {
      const key = el.getAttribute("data-i18n");
      el.textContent = this.t(key);
    });
    document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
      el.placeholder = this.t(el.getAttribute("data-i18n-placeholder"));
    });
    document.querySelectorAll("[data-i18n-title]").forEach((el) => {
      el.title = this.t(el.getAttribute("data-i18n-title"));
    });
    // Update lang toggle button
    const toggleBtn = document.getElementById("lang-toggle");
    if (toggleBtn) {
      toggleBtn.textContent = this.currentLang === "id" ? "EN" : "ID";
    }
    document.documentElement.lang = this.currentLang;
  },

  init() {
    this.applyToPage();
    const toggleBtn = document.getElementById("lang-toggle");
    if (toggleBtn) {
      toggleBtn.addEventListener("click", () => {
        this.setLang(this.currentLang === "id" ? "en" : "id");
      });
    }
  },
};
